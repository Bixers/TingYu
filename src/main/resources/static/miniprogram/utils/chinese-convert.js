/**
 * 繁体转简体
 */
function toSimplified(text) {
  if (!text) return text
  var result = ''
  for (var i = 0; i < text.length; i++) {
    var ch = text[i]
    result += t2sMap[ch] || ch
  }
  return result
}

/**
 * 简体转繁体
 */
function toTraditional(text) {
  if (!text) return text
  var result = ''
  for (var i = 0; i < text.length; i++) {
    var ch = text[i]
    result += s2tMap[ch] || ch
  }
  return result
}

/**
 * 根据当前偏好转换文本
 * @param {string} text 原始文本
 * @param {boolean} useTraditional 是否使用繁体
 * @returns {string}
 */
function convert(text, useTraditional) {
  if (!text) return text
  return useTraditional ? toTraditional(text) : toSimplified(text)
}

